// Operator (연산자)

// 산술 연산자: 덧셈, 뺄셈, 곱셈, 나눗셈, 나머지
console.log(10 + 10); // 20
console.log(10 - 10); // 0
console.log(10 * 10); // 100
console.log(10 / 10); // 1
console.log(10 % 10); // 0

console.log(10 * (10 + 10));

// 증가와 감소
let number = 1;
number++;
console.log(number); // 2

number--;
console.log(number); // 1
